public class StudentInfo {
    public static void main(String[] args) {
        // String holds a sequence of characters (text).
        String name = "Priya Sharma";
        // int is used for whole numbers.
        int age = 21;
        // double represents decimal/floating-point numbers.
        double gpa = 8.8;
        // char stores a single character, enclosed in single quotes.
        char firstInitial = 'P';

        // Output each variable on its own line with a label.
        System.out.println("Student Name: " + name);
        System.out.println("Age: " + age);
        System.out.println("GPA: " + gpa);
        System.out.println("Initial: " + firstInitial);
    }
}

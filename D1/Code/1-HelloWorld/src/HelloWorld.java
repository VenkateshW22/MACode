// Every Java program is organized as a class.
// The class name must match the filename (HelloWorld.java).
public class HelloWorld {
    // The main() method is the entry point for any standalone Java application.
    // It's where program execution begins.
    public static void main(String[] args) {
        // System.out.println is a standard Java method for printing a line to the terminal.
        System.out.println("Hello World!");

    }
}
